#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*p;
	int		i;

	i = 0;
	p = s;
	while (n > 0)
	{
		p[i] = '\0';
		i++;
		n--;
	}
}
