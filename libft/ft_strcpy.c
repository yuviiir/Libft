#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	int		len;

	if (src == NULL && dst == NULL)
		return (NULL);
	i = 0;
	len = 0;
	while (src[len] != '\0')
		len++;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
